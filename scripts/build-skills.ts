// ── Small formatting helpers ─────────────────────────────────────────────────

function table(headers: string[], rows: string[][]): string {
  const head = `| ${headers.join(' | ')} |`;
  const sep = `| ${headers.map(() => '---').join(' | ')} |`;
  const body = rows.map(r => `| ${r.map(cell => cell.replace(/\|/g, '\\|')).join(' | ')} |`).join('\n');
  return `${head}\n${sep}\n${body}`;
}

function code(s: string): string {
  return '`' + s + '`';
}

/** Resolve a single `var(--orbit-…)` reference against a stripped-name lookup. */
function resolveVar(value: string | number, lookup: Record<string, string | number>): string {
  const s = String(value);
  const m = s.match(/^var\(--orbit-([\w-]+)\)$/);
  if (m) {
    const key = m[1];
    if (key in lookup) return String(lookup[key]);
  }
  return s;
}

