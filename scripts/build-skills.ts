function buildOrbitTokensSkill(tokens: Tokens): OutFile[] {
  const files: OutFile[] = [];

  // Lookups for resolving var() references in nested token values.
  const spacingLookup: Record<string, string | number> = {};
  for (const [k, v] of Object.entries(tokens.spacing)) spacingLookup[`space-${k}`] = v;
  const typoVars = tokens.components?.typography ?? {};

  // ---- SKILL.md ----
  const colorCount = Object.keys(tokens.colors).length;
  const skill = `---
name: orbit-tokens
description: ${ORBIT_TOKENS_DESCRIPTION}
---
${GEN_BANNER}

# Orbit tokens

Orbit is Efficio's design system for AI-orchestrated procurement intelligence. This
skill is the authoritative source for Orbit's **design tokens** — the exact colour,
spacing, typography, radius, and elevation values, and the rules for referencing them.

## How to reference tokens

- Tokens are **CSS custom properties** prefixed \`--orbit-\`. Reference them with
  \`var(--orbit-…)\` (or, in \`DESIGN.md\` YAML, \`{colors.token}\`).
- **Never hard-code a raw value** (hex, px, etc.) in a reusable component — the
  static audit (\`npm run audit:design-system\`) fails on raw hex in
  \`packages/orbit/src\`. Reach for the semantic token first, then the primitive.
- Prefer **semantic** colour tokens (\`text-*\`, \`border-*\`, \`bg-*\`) over primitives
  for anything that conveys meaning; primitives are the underlying palette.

## Theme

Orbit is **light-only — there is no dark mode.** The base \`:root\` palette ("CP
mode") is **canonical**. A single brand variant, **Orbit mode**, is activated by
\`data-theme="orbit"\` on an ancestor and re-skins some colours; it is a re-skin, not
a light/dark toggle. Its overrides are documented in \`references/theme-orbit.md\`.

## Token groups (load the reference you need)

- **Colours** (${colorCount} tokens: primitives + semantic) → \`references/colors.md\`
- **Spacing** scale (numeric + named \`none\`→\`mega\`) → \`references/spacing.md\`
- **Typography** (families, sizes, weights, leadings, composite text styles) → \`references/typography.md\`
- **Radius, shadow, z-index, focus ring** → \`references/radius-elevation.md\`
- **Orbit-mode brand overrides** → \`references/theme-orbit.md\`
`;
  files.push({ rel: 'orbit-tokens/SKILL.md', content: skill });

  // ---- references/colors.md ----
  const semanticKeys: string[] = [];
  const primitiveKeys: string[] = [];
  for (const key of Object.keys(tokens.colors)) {
    if (/^(text|border|bg)-/.test(key)) semanticKeys.push(key);
    else primitiveKeys.push(key);
  }
  const role = (k: string): string =>
    k.startsWith('text-') ? 'Text' : k.startsWith('border-') ? 'Border' : k.startsWith('bg-') ? 'Background' : '';
  const semanticRows = semanticKeys.map(k => [code(k), code(`var(--orbit-color-${k})`), tokens.colors[k], role(k)]);
  const primitiveRows = primitiveKeys.map(k => [code(k), code(`var(--orbit-color-${k})`), tokens.colors[k]]);

  const colorsMd = `${GEN_BANNER}

# Orbit colours

Canonical **CP/base** palette (Orbit is light-only). Reference via
\`var(--orbit-color-<token>)\` — never a raw hex. Orbit-mode overrides are in
\`theme-orbit.md\`.

## Semantic colours

Use these for anything meaningful. They alias the primitives below.

${table(['Token', 'CSS variable', 'Value', 'Role'], semanticRows)}

## Primitive palette

The underlying ramp. Prefer a semantic token; use a primitive only when no semantic
token fits.

${table(['Name', 'CSS variable', 'Value'], primitiveRows)}
`;
  files.push({ rel: 'orbit-tokens/references/colors.md', content: colorsMd });

  // ---- references/spacing.md ----
  const numericKeys = Object.keys(tokens.spacing).filter(k => !String(tokens.spacing[k]).startsWith('var('));
  const aliasKeys = Object.keys(tokens.spacing).filter(k => String(tokens.spacing[k]).startsWith('var('));
  const numericRows = numericKeys.map(k => [code(k), String(tokens.spacing[k]), code(`var(--orbit-space-${k})`)]);
  const aliasRows = aliasKeys.map(k => {
    const resolved = resolveVar(tokens.spacing[k], spacingLookup);
    const refMatch = String(tokens.spacing[k]).match(/^var\(--orbit-space-([\w-]+)\)$/);
    const refKey = refMatch ? refMatch[1] : '';
    return [code(k), resolved, code(`var(--orbit-space-${k})`), refKey ? code(refKey) : ''];
  });

  const spacingMd = `${GEN_BANNER}

# Orbit spacing

A single spacing scale, exposed twice: bare numeric steps and named aliases that
point at them. Use the **named aliases** in components for intent
(\`var(--orbit-space-base)\`), the numeric steps when a specific value is needed.

## Scale

${table(['Step', 'Value', 'CSS variable'], numericRows)}

## Named aliases

${table(['Alias', 'Value', 'CSS variable', 'Maps to step'], aliasRows)}
`;
  files.push({ rel: 'orbit-tokens/references/spacing.md', content: spacingMd });

  // ---- references/typography.md ----
  const families = Object.entries(typoVars).filter(([k]) => k.startsWith('font-family-'));
  const sizes = Object.entries(typoVars).filter(([k]) => /^text-(xs|sm|base|lg|xl|2xl|3xl)$/.test(k));
  const weights = Object.entries(typoVars).filter(([k]) => k.startsWith('font-weight-'));
  const leadings = Object.entries(typoVars).filter(([k]) => k.startsWith('leading-'));

  const styleRows = Object.entries(tokens.typography).map(([name, t]) => [
    code(name),
    resolveVar(t.fontFamily, typoVars),
    resolveVar(t.fontSize, typoVars),
    resolveVar(t.fontWeight, typoVars),
    resolveVar(t.lineHeight, typoVars),
  ]);

  const typographyMd = `${GEN_BANNER}

# Orbit typography

Two families: a workhorse sans (Inter) for UI and a brand face (Sofia Pro) for
display moments. Sizes, weights, and line-heights are tokenised; composite text
styles bundle them per role.

## Font families

${table(['Token', 'Value'], families.map(([k, v]) => [code(`--orbit-${k}`), String(v)]))}

## Sizes

${table(['Token', 'Value'], sizes.map(([k, v]) => [code(`--orbit-${k}`), String(v)]))}

## Weights

${table(['Token', 'Value'], weights.map(([k, v]) => [code(`--orbit-${k}`), String(v)]))}

## Line heights

${table(['Token', 'Value'], leadings.map(([k, v]) => [code(`--orbit-${k}`), String(v)]))}

## Composite text styles

Resolved values shown for convenience; in code these are token references.

${table(['Style', 'Family', 'Size', 'Weight', 'Line height'], styleRows)}
`;
  files.push({ rel: 'orbit-tokens/references/typography.md', content: typographyMd });

  // ---- references/radius-elevation.md ----
  const elevation = tokens.components?.elevation ?? {};
  const radiusRows = Object.entries(tokens.rounded).map(([k, v]) => [code(k), String(v), code(`var(--orbit-radius-${k})`)]);
  const shadowRows = Object.entries(elevation)
    .filter(([k]) => k.startsWith('shadow-'))
    .map(([k, v]) => [code(`--orbit-${k}`), String(v)]);
  const zRows = Object.entries(elevation)
    .filter(([k]) => k.startsWith('z-'))
    .map(([k, v]) => [code(`--orbit-${k}`), String(v)]);
  const focusRows = Object.entries(elevation)
    .filter(([k]) => k.includes('focus'))
    .map(([k, v]) => [code(`--orbit-${k}`), String(v)]);

  const radiusElevationMd = `${GEN_BANNER}

# Orbit radius & elevation

Orbit is a **mostly flat** system: hierarchy comes from tonal layering and borders,
not shadow. Shadows are reserved for genuinely floating UI (dropdowns, popovers,
tooltips, modals, toasts).

## Border radius

${table(['Token', 'Value', 'CSS variable'], radiusRows)}

> ⚠ **Known gap:** \`rounded.full\` / \`--orbit-radius-pill\` is referenced by 6
> components (Avatar, Filter, StepCircle, SideNav, Radio, MultiSelectDropdown) but
> is **not defined** in the token source. Until a \`--orbit-radius-pill\` token is
> added, those components rely on a CSS fallback. Do not invent a value — treat
> "full/pill" as an undefined token pending a fix.

## Shadows

${table(['Token', 'Value'], shadowRows)}

## Z-index

${table(['Token', 'Value'], zRows)}

## Focus ring

${focusRows.length ? table(['Token', 'Value'], focusRows) : '_(none)_'}
`;
  files.push({ rel: 'orbit-tokens/references/radius-elevation.md', content: radiusElevationMd });

  // ---- references/theme-orbit.md ----
  // Build a base-value lookup spanning every token group so override base
  // values resolve whether they live in colors, components, typography, etc.
  const baseLookup: Record<string, string> = {};
  for (const [k, v] of Object.entries(tokens.colors)) {
    baseLookup[`color-${k}`] = String(v);
    baseLookup[k] = String(v);
  }
  for (const group of Object.values(tokens.components ?? {})) {
    for (const [k, v] of Object.entries(group)) {
      if (!(k in baseLookup)) baseLookup[k] = String(v);
    }
  }

  const overrides = tokens.themeOverrides ?? {};
  const colorOverrideKeys = Object.keys(overrides).filter(k => k.startsWith('color-'));
  const otherOverrideKeys = Object.keys(overrides).filter(k => !k.startsWith('color-'));

  const colorOverrideRows = colorOverrideKeys.map(k => {
    const name = k.replace(/^color-/, '');
    const base = baseLookup[k] ?? baseLookup[name] ?? '—';
    return [code(name), code(`var(--orbit-color-${name})`), base, String(overrides[k])];
  });
  const otherOverrideRows = otherOverrideKeys.map(k => {
    const base = baseLookup[k] ?? '—';
    return [code(`--orbit-${k}`), base, String(overrides[k])];
  });

  const themeOrbitMd = `${GEN_BANNER}

# Orbit mode (brand variant)

**This is a re-skin, NOT a dark mode.** Orbit is light-only. "Orbit mode" is an
alternative theme from Figma activated by \`data-theme="orbit"\` on an ancestor
element; the prototypes render under it. It overrides a subset of tokens
(saturated status/semantic foregrounds, heading weight, some chrome sizing) on top
of the canonical **CP/base** palette. When the theme is **not** active, the CP/base
values apply.

The base (CP) value is canonical; the tables show what each token becomes under
Orbit mode.

## Colour overrides

${colorOverrideRows.length ? table(['Token', 'CSS variable', 'CP/base value', 'Orbit-mode value'], colorOverrideRows) : '_(none)_'}

## Non-colour overrides (typography & chrome)

${otherOverrideRows.length ? table(['CSS variable', 'CP/base value', 'Orbit-mode value'], otherOverrideRows) : '_(none)_'}
`;
  files.push({ rel: 'orbit-tokens/references/theme-orbit.md', content: themeOrbitMd });

  return files;
}

// ── Source readers ───────────────────────────────────────────────────────────

function readRepo(rel: string): string {
  return readFileSync(join(REPO_ROOT, rel), 'utf-8');
}
function readRepoSafe(rel: string): string {
  const abs = join(REPO_ROOT, rel);
  return existsSync(abs) ? readFileSync(abs, 'utf-8') : '';
}

/** Count exported component categories (src subdirs with an index.ts) and the
 *  total component count from the generated manifest. */
function componentStats(): { count: number; categories: string[] } {
  const srcDir = join(REPO_ROOT, 'packages', 'orbit', 'src');
  const categories = existsSync(srcDir)
    ? readdirSync(srcDir).filter(d => {
        try {
          return statSync(join(srcDir, d)).isDirectory() && existsSync(join(srcDir, d, 'index.ts'));
        } catch {
          return false;
        }
      })
    : [];
  const manifest = readRepoSafe('orbit-manifest.md');
  const m = manifest.match(/Total component count:\**\s*(\d+)/);
  const count = m ? parseInt(m[1], 10) : 0;
  return { count, categories };
}

